const a = 7
let b = 3

// atribuição aditiva

b += a // b = b + a
console.log(`aditiva---> `, b) 

// atribuição subtrativa

b -= 4 // b = b - 4
console.log(`subtrativa---> `, b)

// atribuiçao multiplicativa

b *= 2 // b = b * 2
console.log(`multiplicativa---> `, b)

// atribuição divisiva 

b /= 2 // b = b / 2
console.log(`divisiva---> `, b)

// atribuição modular (resto da divisão)

b %= 2 // b = b % 2
console.log(`resto da divisão---> `, b)
